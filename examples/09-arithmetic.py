# can mix and match number types. Follows order of ops
number = 1 + 2 * 3 / 4.0
print(number)

# you can modula
print(124 % 3)

# 5 ^ 3rd power
print(5 ** 3)

# Floor division:
print(9 // 2)     # prints: 4
print(9.0 // 2.0) # prints: 4.0
print(-11 // 3)   # prints: -4
print(-11.0 // 3) # prints: -4.0
